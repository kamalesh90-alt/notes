#Integer

mobileNo = 987563210
print('Integer Datatype :',mobileNo,type(mobileNo))

Temp = 987563210
print('Float Datatype :',Temp,type(Temp))

Complexnumber = 20j+20
print('Complex Datatype :',Complexnumber,type(Complexnumber))


#Sequence Data Types
course = 'Python'
print('String Data Type :',course,type(course))

course = ['Python','Java','React']
print('List Data Type :',course,type(course))

course = ('Python','Java','React')
print('tuple Data Type :',course,type(course))

#Boolean 
is_pass = True
print('Boolean Data type',is_pass,type(is_pass))

#Set

course = {'Python','Java','React'}
print('Set Data Type :',course,type(course))

#Dictionary
watch_details = {
    'Titan ':4000,
    'Fastrack':5000
}

print('Dictionary Data Type :',watch_details,type(watch_details))
